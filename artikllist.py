# Считываем входную строку текста
tekst = input()

# Разрезаем строку на список слов по пробелам
vse_slova = tekst.split()

# Создаем переменную для хранения суммы найденных артиклей
vsego_artikley = 0

# Список артиклей, которые мы ищем (в нижнем регистре)
nuzhnie_artikli = ['a', 'an', 'the']

# Проходим циклом по каждому слову из текста
for slovo in vse_slova:
    # Приводим слово к нижнему регистру, чтобы поиск был точным
    if slovo.lower() in nuzhnie_artikli:
        vsego_artikley += 1

# Выводим результат в нужном формате
print("Общее количество артиклей:", vsego_artikley)