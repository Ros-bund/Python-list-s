# Считываем строку с IP-адресом (например: "192.168.1.1")
vvedenniy_ip = input()

# Разбиваем строку на части, используя точку как разделитель
spisok_chastiey = vvedenniy_ip.split('.')

# Создаем флажок, который по умолчанию считает адрес правильным
vse_horosho = True

# Проходим по каждой части нашего IP
for chast in spisok_chastiey:
    # Превращаем текст в целое число
    chislo = int(chast)
    
    # Проверяем, входит ли число в допустимый диапазон [0, 255]
    if not (0 <= chislo <= 255):
        # Если хотя бы одно число не подходит, меняем флажок и выходим
        vse_horosho = False
        break

# Выводим результат в зависимости от состояния флажка
if vse_horosho:
    print("ДА")
else:
    print("НЕТ")